
let valor1 = parseInt(prompt("Ingrese el primer numero"));
let valor2 = parseInt(prompt("Ingrese el segundo numero"));


if (valor1, valor2 === null) {
    console.log('A casa pete')
} else if (isNaN(valor1, valor2)){
    console.log('No a selecionado ningun valor')
} else {
    console.log(`${valor1} + ${valor2} = ${valor1 + valor2}`)
    console.log(`${valor1} - ${valor2} = ${valor1 - valor2}`)
    console.log(`${valor1} * ${valor2} = ${valor1 * valor2}`)
    console.log(`${valor1} / ${valor2} = ${valor1 / valor2}`)
}